import java.util.*;
public class DIV
{
  public static void main(String args[])
  {
    int div1,div2;
    Scanner sc=new Scanner(System.in);
    int count=0;
    int c=div1-div2;
    while(c<=0)
    {
      int t;
      t=c;
      div1=t;
      c=div1-div2;
      count++;
    }
    println("the quotient number is"+count);
  }
}
